        <script>
            var currentPage = 1;
            var perPage = 6;
            var totalBlogs = {{ $blogs->count() }};
            var loadMoreButton = document.getElementById('load-more-btn');
            var blogContainer = document.getElementById('blog-container');

            loadMoreButton.addEventListener('click', function() {
                var startIndex = currentPage * perPage;
                var endIndex = startIndex + perPage;
                if (endIndex >= totalBlogs) {
                    endIndex = totalBlogs;
                    loadMoreButton.style.display = 'none';
                }
                for (var i = startIndex; i < endIndex; i++) {
                    var blog = {!! $blogs->toJson() !!}[i];
                    var blogHtml = `
                        <div class="col-xl-4 col-lg-6">
                            <div class="card bg-light rounded overflow-hidden">
                                ${blog.tipo === 'imagen' ? `<img style="width: 100%; height: 216px;" src="${blog.archivo}" alt="">` : ''}
                                ${blog.tipo === 'video' ? `
                                    <div style="width: 100%; height: 216px; position: relative;">
                                        <video
                                            autoplay="true"
                                            muted="true"
                                            preload="auto"
                                            loop="loop"
                                            controls>
                                            <source src="${blog.archivo}" type="video/mp4">
                                            Tu navegador no admite el elemento de video.
                                        </video>
                                    </div>
                                ` : ''}
                                <div class="card-body">
                                    <a class="h3 d-block mb-3" href="">${blog.titulo}</a>
                                    <p class="card-text">${blog.descripcion.replace(/\n/g, '<br>')}</p>
                                </div>
                            </div>
                        </div>
                    `;
                    blogContainer.innerHTML += blogHtml;
                }
                currentPage++;
            });
        </script>
